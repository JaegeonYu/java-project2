package com.example.javaproject2.codeup;

import java.util.Scanner;

public class Pro1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (true) {
            int val = sc.nextInt();
            if (val == 0) break;
            System.out.println(val);
        }
    }
}
